export const totalMes = (año, mes) => {
    try {
        const movimientosMes = dataPruebas[0].movimientos_por_año[año].meses[mes.toLowerCase()].movimientos;

        let totales = {
            ingresos: 0,
            gastos: 0
        };

        movimientosMes.forEach(movimiento => {
            const cantidad = parseFloat(movimiento.cantidad.replace(',', '.'));

            if (cantidad > 0) {
                totales.ingresos += cantidad;
            } else {
                totales.gastos += Math.abs(cantidad);
            }
        });

        return totales;
    } catch (error) {
        console.error(`Error al calcular totales para ${mes} ${año}:`, error);
        return { ingresos: 0, gastos: 0 };
    }
};

export const totalAño = (año) => {
    try {
        const meses = Object.keys(dataPruebas[0].movimientos_por_año[año].meses);
        
        let totalesAnuales = {
            ingresos: 0,
            gastos: 0
        };
        
        meses.forEach(mes => {
            const totalesMes = totalMes(año, mes);
            totalesAnuales.ingresos += totalesMes.ingresos;
            totalesAnuales.gastos += totalesMes.gastos;
        });
        
        return totalesAnuales;
    } catch (error) {
        console.error(`Error al calcular totales para el año ${año}:`, error);
        return { ingresos: 0, gastos: 0 };
    }
};